function foundMoreThanOnce(arr) {
    let counts = {}; // Об'єкт для підрахунку кількості повторень елементів
    
       // Підраховуємо, скільки разів зустрічається кожен елемент
    arr.forEach(item => {
        counts[item] = (counts[item] || 0) + 1;
    });

       // Фільтруємо лише ті елементи, які зустрічаються більше одного разу
    return arr.filter(item => counts[item] > 1);

}

