function isPalindrome(str) {
  // Очищаємо рядок від пробілів і приводимо до нижнього регістру
  const cleanedStr = str.replace(/\s+/g, '').toLowerCase();
  
  // Порівнюємо оригінальний очищений рядок із його зворотнім варіантом
  return cleanedStr === cleanedStr.split('').reverse().join('');
}

